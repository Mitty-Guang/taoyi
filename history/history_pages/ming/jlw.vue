              <br />&emsp;&emsp;隆庆朝由于时间短，因而器物也少，大器、带年款器则更少。青料用回青，呈色稳定、纯正，蓝中泛紫（不像嘉靖器般紫得厉害）。纹饰与嘉靖朝相近。因以小件器为主，胎、釉较细腻。多六角、八角等造型。官窑器的款识有“大明隆庆年造”和“隆庆年造”两种，不见“制”款；民窑器款有“隆庆年造”或“制”四字款；吉语款有万福攸同、福寿康宁等，颂语款有富贵佳器等。
              <br />&emsp;&emsp;万历早期用回青，中晚期用石子青及浙料。多淡描青花。纹饰除传统的外，还流行锦地开光纹饰；纹饰布局繁密，主题不清；另外福禄寿字为纹饰的也多见。瓶类较多见镂空、套活环等工艺。器型除传统的外，新出现了壁瓶。胎质较粗，釉白中泛青。款识多见“大明万历年造”，也有“大明万历年制”、“万历年造”；多伪托款，有宣德、成化、嘉靖的。总的来说工艺水平较前退步。
