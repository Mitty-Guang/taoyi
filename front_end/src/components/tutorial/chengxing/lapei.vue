              <!-- &emsp;&emsp; -->
              1．简介<br />
              &emsp;&emsp;拉坯成型是最为广泛应用也是最方便的一种成型方法，拉坯成型是陶瓷历史上一个重大的革命，不仅提高了工作效率，而且制作的器物更加完美和精致。拉坏成型是在快速转动的轮子上，将手探进柔软的黏土中，借助螺旋运动的惯性，让黏土向外扩展，向上推升，形成环形的还体的过程。拉坯成型法，主要制作一些同心圆的造型。如：碗、盘子、罐子等。但现代陶艺则通过对拉还成型的作品进行扭曲、镂空、挤压之后在进行拼合，创造出新的富有特色的作品。<br />
              2．方法<br />
              （1）将泥团放在转盘中央，启动拉坯机。<br />
              （2）双手用力将泥团扶正。<br />
              （3）将泥团向上提拉。将手指深入中央，形成窝状。一只手扶住外形，另一只手深入中央使内部扩大。将维形向上提拉逐渐改变外形。双手将口沿向外扩展。<br />
              （4）将手指深入中央，形成窝状。<br />
              （5）一只手扶住外形，另一只手深入中央使内部扩大。<br />
              （6）将雏形向上提拉逐渐改变外形。<br />
              （7）双手将口沿向外扩展。<br />
              （8）用泥巴修整外形。<br />
              （9）用细钢丝切割转盘上的还体，拉坯作品完成。<br />
              3．注意事项<br />
              （1）拉坯用的泥略干为好，特别是拉制较大作品，因为泥料过湿，站立性弱。<br />
              （2）拉坯时，作品的尺度应考虑坯体的收缩系数，适当放大。<br />
              （3）拉坯时要用力均匀，否则拉制出的坯体烧后会在器皿表面产生旋纹缺陷。<br />
              4．特点<br />
              &emsp;&emsp;拉坯成型制作出来的造型，挺拔、规整。但它是一种技术较强的成型工艺，对泥的干湿性、轮子转速的快慢以及于的动作的运用等，都有较高的技术要求，对于初学者来说，要经过相当长的时间进行训练，才能掌握这种成型技术。<br />
